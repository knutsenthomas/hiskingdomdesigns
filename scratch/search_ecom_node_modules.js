import fs from 'fs';
import path from 'path';

function searchDirectory(dir, term) {
  const files = fs.readdirSync(dir);
  for (const file of files) {
    const fullPath = path.join(dir, file);
    const stat = fs.statSync(fullPath);
    if (stat.isDirectory()) {
      searchDirectory(fullPath, term);
    } else if (file.endsWith('.js') || file.endsWith('.ts') || file.endsWith('.json')) {
      const content = fs.readFileSync(fullPath, 'utf8');
      if (content.includes(term)) {
        console.log(`Found "${term}" in ${fullPath}`);
        // Log around the term
        const lines = content.split('\n');
        lines.forEach((line, idx) => {
          if (line.includes(term)) {
            console.log(`  L${idx + 1}: ${line.trim()}`);
          }
        });
      }
    }
  }
}

console.log('Searching node_modules/@wix/ecom for "catalogReference"...');
searchDirectory('./node_modules/@wix/ecom', 'catalogReference');
console.log('Search finished.');
